    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String rutaArchivo = "./src/main/resources/VuelosDiarios.csv";

        String[] datos = {codigosVuelos, txtFecha.getText(), txtHorasSalida.getText(), txtHoraLlegada.getText(), txtPlazasOcu.getText(),txtPrecio.getText()};
        try (CSVWriter csvWriter = new CSVWriter(new FileWriter(rutaArchivo,true))) {
            csvWriter.writeNext(datos);
            System.out.println("Se han escrito los datos en el archivo CSV correctamente.");

        } catch (IOException ex) {
            System.err.println("Error al escribir en el archivo CSV: " + ex.getMessage());
        }

        txtFecha.setText(null);
        txtHorasSalida.setText(null);
        txtHoraLlegada.setText(null);
        txtPlazasOcu.setText(null);
        txtPrecio.setText(null);
    }//GEN-LAST:event_btnAddActionPerformed

