    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
         String rutaArchivo = "./src/main/resources/VuelosBase.csv";

        String[] datos = {txtCodigo.getText(), nombreCompanyaOrigen, nombreCompanyaDestino, txtNumeroPlazas.getText(), txtHoraS.getText(), txtHoraL.getText(),txtDias.getText()};
        try (CSVWriter csvWriter = new CSVWriter(new FileWriter(rutaArchivo,true))) {
            csvWriter.writeNext(datos);
            System.out.println("Se han escrito los datos en el archivo CSV correctamente.");

        } catch (IOException ex) {
            System.err.println("Error al escribir en el archivo CSV: " + ex.getMessage());
        }
        
        txtCodigo.setText(null);
        txtNumeroPlazas.setText(null);
        txtHoraS.setText(null);
        txtHoraL.setText(null);
        txtDias.setText(null);

    }//GEN-LAST:event_btnAddActionPerformed

